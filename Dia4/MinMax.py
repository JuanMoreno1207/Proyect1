# Funcion que nos permite sacar de un iterable el valor menor o mayor
lista = [12,58,26,48]
minimo = min(lista)
mayor = max(lista)
print(f"El mayor es {mayor} y el minimo es {minimo}")

# funciona igualmente para iterables string (La mayuscula es mayor a las minusculas)
lista = ['Juan','Viviana']
minimo = min(lista)
mayor = max(lista)
print(f"El mayor es {mayor} y el minimo es {minimo}")